/**
 * Defines the scalings in x, y and z dimension
 */
export class Scale {

    /**
     * @constructor
     * @param {number} x x scaling
     * @param {number} y y scaling
     * @param {number} z z scaling
     */
    constructor(x, y, z) {

        /**
         * x scaling
         * @type {number}
         */
        this.x = x;

        /**
         * y scaling
         * @type {number}
         */
        this.y = y;

        /**
         * z scaling
         * @type {number}
         */
        this.z = z;

    }

}